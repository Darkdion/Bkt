<script>
    $(function(){
        // SLIDE FEATURES //
        $(".apply-step").each(function(){
            var _slide = $(this),
                _pagination = _slide.find(".slide-pagination").find("li"),
            //_pagination = _slide.find(".slide-pagination1").find("li"),
                _h3 = _pagination.find("h3"),
                _pane = _slide.find(".slide-pane"),
                _scene = _pane.find(".slide-scene"),
                _next = _slide.find('.slide-next'),
                _prev = _slide.find('.slide-prev'),
                _allow = true,
                _temp = 0, _cache = 0, _max = _pagination.length;
            _tempObject = $(_scene[0]);

            _pagination.click(function(e){
                e.preventDefault();
                var i = $(this).index();
                if((_temp==i)||!_allow)return false;
                _allow = false;
                _pagination.removeClass("active");
                _h3.removeClass("setBorderColor");
                $(this).addClass("active");
                $(this).find("h3").addClass("setBorderColor");
                $(_scene[i]).addClass("show");
                var _wd = _pane.width();
                if(_temp>i){
                    _pane.stop().scrollLeft(_wd).animate({
                        'scrollLeft':0
                    },450,"easeInOutSine",function(){
                        _allow = true;
                        $(_scene[_cache]).removeClass("show");
                        /*_tempObject = $(_scene[_temp]);*/
                    });
                }else{
                    _pane.stop().animate({
                        'scrollLeft':_wd
                    },450,"easeInOutSine",function(){
                        _allow = true;
                        $(_scene[_cache]).removeClass("show");
                        $(this).scrollLeft(0);
                        /*_tempObject = $(_scene[_temp]);*/
                    });
                }
                //
                _cache = _temp;
                _temp = i;
                _next.show();
                _prev.show();
                switch(true){
                    case (i==0):_prev.hide();break;
                    case (i==_max-1):_next.hide();break;
                }
            });
            _prev.click(function(){
                if(!_allow||(_temp==0))return false;
                var i = _temp-1;
                _allow = false;
                var _obj = $(_pagination[i]),
                    _wd = _pane.width();
                _pagination.removeClass("active");
                _h3.removeClass("setBorderColor");
                _obj.addClass("active");
                _obj.find("h3").addClass("setBorderColor");
                $(_scene[i]).addClass("show");
                _pane.stop().scrollLeft(_wd).animate({
                    'scrollLeft':0
                },450,"easeInOutSine",function(){
                    _allow = true;
                    $(_scene[_cache]).removeClass("show");
                    /*_tempObject = $(_scene[_temp]);*/
                });
                _cache = _temp;
                _temp = i;
                _next.show();
                if(i==0)$(this).hide();
            });
            _next.click(function(){
                if(!_allow||(_temp==_max-1))return false;
                var i = _temp+1;
                _allow = false;
                var _obj = $(_pagination[i]),
                    _wd = _pane.width();
                _pagination.removeClass("active");
                _h3.removeClass("setBorderColor");
                _obj.addClass("active");
                _obj.find("h3").addClass("setBorderColor");
                $(_scene[i]).addClass("show");
                _pane.stop().animate({
                    'scrollLeft':_wd
                },450,"easeInOutSine",function(){
                    _allow = true;
                    $(_scene[_cache]).removeClass("show");
                    $(this).scrollLeft(0);
                    /*_tempObject = $(_scene[_temp]);*/
                });
                _cache = _temp;
                _temp = i;
                _prev.show();
                if(i==_max-1)$(this).hide();
            });

            _prev.hide();
        });
    });
</script>

